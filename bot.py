        # –°–∫–ª–∞–¥—ã–≤–∞–µ–º –≤—Å–µ —Ü–∏—Ñ—Ä—ã –¥–∞—Ç—ã
        total = day + month + year
        
        # –ü—Ä–µ–≤—Ä–∞—â–∞–µ–º —Å—É–º–º—É –≤ –æ–¥–Ω—É —Ü–∏—Ñ—Ä—É (—Å–∫–ª–∞–¥—ã–≤–∞–µ–º —Ü–∏—Ñ—Ä—ã —á–∏—Å–ª–∞, –ø–æ–∫–∞ –Ω–µ –ø–æ–ª—É—á–∏–º 1-9 –∏–ª–∏ 11, 22, 33)
        while total > 9 and total not in [11, 22, 33]:
            total = sum(int(digit) for digit in str(total))
            
        return total
